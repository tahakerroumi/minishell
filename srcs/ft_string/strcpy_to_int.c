#include "minishell.h"

void	strcpy_to_int(int *dest, const char *src)
{
	int	i;

	if (src)
	{
		i = 0;
		while (src[i])
		{
			dest[i] = (int)src[i];
			i++;
		}
		dest[i] = 0;
	}
}
